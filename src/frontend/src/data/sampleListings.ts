export function formatRent(rent: number, city: string): string {
  const info = currencyMap[city] ?? { symbol: "₹", locale: "en-IN" };
  const formatted = rent.toLocaleString(info.locale);
  // For multi-char symbols add a space
  const spacer = info.symbol.length > 1 ? " " : "";
  return `${info.symbol}${spacer}${formatted}`;
}

